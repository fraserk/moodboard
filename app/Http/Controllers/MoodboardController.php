     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $categories = Category::pluck('id');
        $data = $categories->flatten();
        $catID = $data->all();
        $moodboard = request()->user()->CreateMoodBoard($request, $catID);
        return redirect()->route('user.moodboard.show', [auth()->user(), $moodboard->id]);
    }

